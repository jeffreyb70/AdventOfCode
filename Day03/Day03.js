//Starting at the top-left corner of your map and following a slope of right 3 and down 1, how many trees would you encounter?

function GetAmountOfTrees(input, rightAmount, DownAmount) {
    const lengthOfRow = input[0].length;

    let amountOfTrees = 0;
    let positionInRow = 0;

    for (let i = DownAmount; i < input.length; i += DownAmount) {
        const element = input[i];

        positionInRow += rightAmount;
        if (positionInRow >= lengthOfRow)
            positionInRow -= lengthOfRow

        const char = element.charAt(positionInRow)

        if (char == "#")
            amountOfTrees++
    }
    console.log(amountOfTrees)
    return amountOfTrees;
}